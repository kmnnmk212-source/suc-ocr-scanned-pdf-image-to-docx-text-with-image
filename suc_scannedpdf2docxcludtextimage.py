def add_element_to_doc(doc: Document, elem: PageElement,
                        page_w_pt: float, page_h_pt: float,
                        content_w_in: float):
    """يضيف عنصراً واحداً إلى مستند الـ DOCX مع الحفاظ على نسبه المكانية."""

    x0, y0, x1, y1 = elem.bbox
    # نسبة العرض
    rel_x   = x0 / page_w_pt          # من أين يبدأ (0→1)
    rel_w   = (x1 - x0) / page_w_pt   # عرضه النسبي
    elem_w_in = content_w_in * rel_w

    # ── نص ──────────────────────────────────────────────────────────────────
    if elem.etype == ElementType.TEXT:
        para = doc.add_paragraph()
        run  = para.add_run(elem.content)
        avg_sz = elem.font_info.get("avg_size", 12)
        run.font.size = Pt(max(6, avg_sz))

        is_arabic = _contains_arabic(elem.content)
        if is_arabic:
            run.font.name = "Arial"
            _set_rtl(para)
            # محاذاة معكوسة للعربية: يمين = بداية النص
            if rel_x > 0.55:
                para.alignment = WD_ALIGN_PARAGRAPH.LEFT
            elif rel_x > 0.25:
                para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            else:
                para.alignment = WD_ALIGN_PARAGRAPH.RIGHT
        else:
            if rel_x > 0.55:
                para.alignment = WD_ALIGN_PARAGRAPH.RIGHT
            elif rel_x > 0.25:
                para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            else:
                para.alignment = WD_ALIGN_PARAGRAPH.LEFT
        # مسافة بادئة بسيطة
        left_indent = Inches(content_w_in * rel_x * 0.7)
        para.paragraph_format.left_indent = min(left_indent, Inches(content_w_in * 0.5))

    # ── صورة / رسم بياني ─────────────────────────────────────────────────────
    elif elem.etype in (ElementType.IMAGE, ElementType.CHART):
        try:
            img_bytes = elem.content
            if not img_bytes:
                print(f"      [تحذير] عنصر صورة بدون بيانات — تم تجاهله")
                return
            img_stream = io.BytesIO(img_bytes)
            # تحقق من صحة الصورة بدون استخدام verify() الذي يُفسد الـ stream في بعض إصدارات Pillow
            pil_img = Image.open(img_stream)
            pil_img.load()          # يقرأ البيانات فعلياً للتأكد من سلامتها
            img_stream.seek(0)      # نعيد المؤشر لأول الستريم بعد القراءة

            para = doc.add_paragraph()
            if rel_x > 0.55:
                para.alignment = WD_ALIGN_PARAGRAPH.RIGHT
            elif rel_x > 0.25:
                para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            else:
                para.alignment = WD_ALIGN_PARAGRAPH.LEFT

            run = para.add_run()
            display_w = min(elem_w_in, content_w_in)
            if display_w <= 0:
                display_w = content_w_in * 0.5
            run.add_picture(img_stream, width=Inches(display_w))
        except Exception as e:
            import traceback
            print(f"      [خطأ] فشل إضافة صورة ({elem.etype.value}): {e}")
            traceback.print_exc()
            para = doc.add_paragraph(f"[فشل استخراج صورة: {e}]")

    # ── جدول ─────────────────────────────────────────────────────────────────
    elif elem.etype == ElementType.TABLE:
        data = elem.content
        if not data:
            return
        # تنظيف البيانات
        rows = [[str(c or "") for c in row] for row in data]
        if not rows or not rows[0]:
            return

        ncols = max(len(r) for r in rows)
        nrows = len(rows)

        tbl = doc.add_table(rows=nrows, cols=ncols)
        tbl.style = "Table Grid"

        for ri, row in enumerate(rows):
            for ci, cell_txt in enumerate(row):
                if ci >= ncols:
                    break
                cell = tbl.cell(ri, ci)
                cell.text = cell_txt
                # تمييز الصف الأول (رأس الجدول)
                if ri == 0:
                    for run in cell.paragraphs[0].runs:
                        run.bold = True

        doc.add_paragraph()   # سطر فراغ بعد الجدول


