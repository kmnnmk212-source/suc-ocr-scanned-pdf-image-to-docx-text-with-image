def convert_pdf_to_docx(input_pdf: str, output_docx: str,
                         save_preview: bool = True,
                         preview_dir: str = None):
    """
    يحوّل ملف PDF إلى DOCX مع الحفاظ على التخطيط المكاني.
    """
    input_pdf  = str(input_pdf)
    output_docx = str(output_docx)

    print(f"\n{'='*60}")
    print(f"  📄 تحليل: {input_pdf}")
    print(f"{'='*60}")

    fitz_doc   = fitz.open(input_pdf)
    doc        = Document()

    # ── إعدادات الصفحة (A4 افتراضي) ────────────────────────────────────────
    section = doc.sections[0]
    section.page_width  = Cm(21)
    section.page_height = Cm(29.7)
    margin = Cm(2.0)
    section.top_margin    = margin
    section.bottom_margin = margin
    section.left_margin   = margin
    section.right_margin  = margin
    content_w_in = (21 - 4) / 2.54   # عرض المحتوى بالإنش

    preview_paths = []

    with pdfplumber.open(input_pdf) as plumber_doc:
        for page_num in range(len(fitz_doc)):
            fitz_page    = fitz_doc[page_num]
            plumber_page = plumber_doc.pages[page_num]

            pw = float(fitz_page.rect.width)
            ph = float(fitz_page.rect.height)

            print(f"\n  ── صفحة {page_num+1} ({pw:.0f}×{ph:.0f} pt) ──")

            # استخراج العناصر
            elements = extract_elements(fitz_page, plumber_page)

            counts = {}
            for e in elements:
                counts[e.etype.name] = counts.get(e.etype.name, 0) + 1
            for k, v in counts.items():
                print(f"      {k}: {v}")

            # صورة المعاينة مع المستطيلات
            if save_preview:
                ann_img = annotate_page_image(fitz_page, elements, scale=2.0)
                if preview_dir:
                    os.makedirs(preview_dir, exist_ok=True)
                    p_path = os.path.join(preview_dir, f"page_{page_num+1:03d}_preview.png")
                else:
                    p_path = os.path.splitext(output_docx)[0] + f"_page{page_num+1}_preview.png"
                ann_img.save(p_path, "PNG")
                preview_paths.append(p_path)
                print(f"      💾 معاينة: {p_path}")

            # إضافة عنوان الصفحة
            if page_num > 0:
                doc.add_page_break()
            heading = doc.add_paragraph(f"صفحة {page_num+1}")
            heading.style = "Heading 2"

            # إضافة العناصر إلى DOCX
            for elem in elements:
                add_element_to_doc(doc, elem, pw, ph, content_w_in)

    # حفظ DOCX
    doc.save(output_docx)
    print(f"\n✅ تم الحفظ: {output_docx}")
    if preview_paths:
        print(f"✅ صور المعاينة: {len(preview_paths)} ملف")

    fitz_doc.close()
    return output_docx, preview_paths


