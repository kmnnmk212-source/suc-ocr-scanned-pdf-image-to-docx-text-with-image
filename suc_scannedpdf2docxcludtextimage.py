# ── أنواع العناصر ──────────────────────────────────────────────────────────────

class ElementType(Enum):
    TEXT  = "text"
    IMAGE = "image"
    TABLE = "table"
    CHART = "chart"   # رسم بياني / شكل معقد


# ألوان مستطيلات التمييز لكل نوع (R, G, B)
ELEMENT_COLORS = {
    ElementType.TEXT:  (0,   120, 215),   # أزرق
    ElementType.IMAGE: (0,   180,  90),   # أخضر
    ElementType.TABLE: (220,  80,  30),   # برتقالي
    ElementType.CHART: (160,  32, 240),   # بنفسجي
}

ELEMENT_LABELS = {
    ElementType.TEXT:  "TEXT  نص",
    ElementType.IMAGE: "IMAGE صورة",
    ElementType.TABLE: "TABLE جدول",
    ElementType.CHART: "CHART رسم بياني",
}


# ── هيكل بيانات العنصر ─────────────────────────────────────────────────────────

@dataclass
class PageElement:
    etype:   ElementType
    bbox:    Tuple[float, float, float, float]   # x0, y0, x1, y1  (PDF units)
    content: object = None     # نص str / صورة bytes / بيانات جدول list
    font_info: dict = field(default_factory=dict)


# ── استخراج العناصر من صفحة ────────────────────────────────────────────────────

def is_scanned_page(fitz_page: fitz.Page) -> bool:
    """يحدد إن كانت الصفحة مجرد صورة ممسوحة (سكانر) بدون نص حقيقي."""
    text = fitz_page.get_text("text").strip()
    images = fitz_page.get_images(full=True)
    if len(text) < 15 and len(images) >= 1:
        # تحقق أن الصورة تغطي معظم الصفحة
        page_area = fitz_page.rect.width * fitz_page.rect.height
        for img_info in images:
            xref = img_info[0]
            rects = fitz_page.get_image_rects(xref)
            for r in rects:
                img_area = (r.x1 - r.x0) * (r.y1 - r.y0)
                if img_area / page_area > 0.6:
                    return True
    return len(text) < 15 and len(images) == 0  # صفحة فارغة تماماً من النص أيضاً تحتاج تحقق


def detect_picture_regions(pil_img: Image.Image, scale: float) -> List[Tuple[float, float, float, float]]:
    """
    يكتشف مناطق الصور/الرسومات داخل صفحة سكانر (وليس النص) عبر تحليل
    كثافة الحواف والتباين المحلي. يرجّع bboxes بوحدات PDF (pt).
    """
    if not CV2_AVAILABLE:
        return []

    gray = cv2.cvtColor(np.array(pil_img), cv2.COLOR_RGB2GRAY)
    h, w = gray.shape

    # حواف قوية (كاني) لتمييز الرسومات كثيفة التفاصيل عن سطور النص الرفيعة
    edges = cv2.Canny(gray, 50, 150)

    # تكثيف بسيط لربط تفاصيل الرسم الداخلية فقط (نواة صغيرة لتجنّب الالتحام بنص الكابشن)
    kernel = cv2.getStructuringElement(cv2.MORPH_RECT, (7, 7))
    dilated = cv2.dilate(edges, kernel, iterations=2)

    contours, _ = cv2.findContours(dilated, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    regions = []
    page_area = w * h
    for cnt in contours:
        x, y, cw, ch = cv2.boundingRect(cnt)
        area = cw * ch
        area_ratio = area / page_area
        if area_ratio < 0.03 or area_ratio > 0.85:
            continue
        aspect = cw / max(ch, 1)
        if aspect > 4.5 or aspect < 0.2:
            continue
        roi_edges = edges[y:y+ch, x:x+cw]
        edge_density = np.count_nonzero(roi_edges) / max(area, 1)
        if edge_density < 0.06:
            continue

        # تقليم الحافة السفلية بدقة: نتحرك من الأسفل لأعلى ونوقف عند أول صف
        # كثيف الحواف بعد عبور صف فارغ كامل (هامش حقيقي بين الرسم والكابشن)
        row_density = (edges[y:y+ch, x:x+cw] > 0).sum(axis=1) / max(cw, 1)
        new_ch = ch
        i = ch - 1
        seen_content = False
        gap_count = 0
        while i >= 0:
            if row_density[i] > 0.02:
                seen_content = True
                gap_count = 0
            elif seen_content:
                gap_count += 1
                if gap_count > max(3, int(0.01 * h)):
                    new_ch = i
                    break
            i -= 1
        ch = max(new_ch, int((ch) * 0.3))  # حد أدنى أماني

        regions.append((x/scale, y/scale, (x+cw)/scale, (y+ch)/scale))

    # دمج المناطق المتداخلة/المتقاربة
    merged = []
    for r in sorted(regions, key=lambda r: -(r[2]-r[0])*(r[3]-r[1])):
        keep = True
        for m in merged:
            ix0, iy0 = max(r[0], m[0]), max(r[1], m[1])
            ix1, iy1 = min(r[2], m[2]), min(r[3], m[3])
            if ix1 > ix0 and iy1 > iy0:
                inter = (ix1-ix0)*(iy1-iy0)
                if inter / min((r[2]-r[0])*(r[3]-r[1]), (m[2]-m[0])*(m[3]-m[1])) > 0.4:
                    keep = False
                    break
        if keep:
            merged.append(r)
    return merged


def ocr_extract_elements(fitz_page: fitz.Page, lang: str = "ara+eng",
                          scale: float = 3.0) -> List["PageElement"]:
    """يستخرج عناصر نصية وصورية من صفحة ممسوحة (سكانر) باستخدام OCR + كشف بصري."""
    mat = fitz.Matrix(scale, scale)
    pix = fitz_page.get_pixmap(matrix=mat, alpha=False)
    img = Image.frombytes("RGB", [pix.width, pix.height], pix.samples)

    elements: List[PageElement] = []

    # ── 1. كشف مناطق الصور/الرسومات بصرياً ─────────────────────────────────
    picture_regions = detect_picture_regions(img, scale)
    for (px0, py0, px1, py1) in picture_regions:
        cx0, cy0 = int(px0*scale), int(py0*scale)
        cx1, cy1 = int(px1*scale), int(py1*scale)
        if cx1 - cx0 < 5 or cy1 - cy0 < 5:
            print(f"      [تحذير] منطقة صورة مكتشفة بأبعاد غير صالحة ({cx1-cx0}×{cy1-cy0}) — تم تجاهلها")
            continue
        try:
            crop = img.crop((cx0, cy0, cx1, cy1))
            buf = io.BytesIO()
            crop.save(buf, format="PNG")
            img_data = buf.getvalue()
            if len(img_data) < 100:
                print(f"      [تحذير] بيانات صورة صغيرة جداً ({len(img_data)} بايت) — تم تجاهلها")
                continue
            elements.append(PageElement(ElementType.IMAGE, (px0, py0, px1, py1), img_data))
        except Exception as e:
            print(f"      [خطأ] فشل قص/حفظ منطقة صورة: {e}")

    if not OCR_AVAILABLE:
        print("  [تحذير] pytesseract غير متاح؛ لن يتم استخراج النص من الصفحات الممسوحة")
        print("          ثبّت: pip install pytesseract --break-system-packages && apt-get install tesseract-ocr tesseract-ocr-ara")
        elements.sort(key=lambda e: (e.bbox[1], e.bbox[0]))
        return elements

    try:
        ocr_data = pytesseract.image_to_data(
            img, lang=lang, output_type=pytesseract.Output.DICT,
            config="--psm 3"
        )
    except Exception as e:
        print(f"  [خطأ OCR] {e}")
        elements.sort(key=lambda e: (e.bbox[1], e.bbox[0]))
        return elements

    def _inside_picture(x0, y0, x1, y1) -> bool:
        cx, cy = (x0+x1)/2, (y0+y1)/2
        for (px0, py0, px1, py1) in picture_regions:
            if px0 <= cx <= px1 and py0 <= cy <= py1:
                return True
        return False

    n = len(ocr_data["text"])
    # تجميع الكلمات حسب رقم الكتلة + الفقرة + السطر لإعادة بناء أسطر كاملة
    lines = {}
    for i in range(n):
        word = ocr_data["text"][i].strip()
        conf_raw = str(ocr_data.get("conf", ["-1"]*n)[i])
        conf = int(conf_raw) if conf_raw.lstrip("-").isdigit() else -1
        if not word or conf < 25:
            continue
        x, y, w, h = (ocr_data["left"][i], ocr_data["top"][i],
                      ocr_data["width"][i], ocr_data["height"][i])
        # استبعاد الكلمات الواقعة داخل منطقة صورة مكتشفة (غالباً ضجيج OCR على الرسم)
        if _inside_picture(x/scale, y/scale, (x+w)/scale, (y+h)/scale):
            continue
        key = (ocr_data["block_num"][i], ocr_data["par_num"][i], ocr_data["line_num"][i])
        if key not in lines:
            lines[key] = {"words": [], "x0": x, "y0": y, "x1": x+w, "y1": y+h}
        ld = lines[key]
        ld["words"].append(word)
        ld["x0"] = min(ld["x0"], x)
        ld["y0"] = min(ld["y0"], y)
        ld["x1"] = max(ld["x1"], x + w)
        ld["y1"] = max(ld["y1"], y + h)

    text_elements = []
    for key in sorted(lines.keys()):
        ld = lines[key]
        text = " ".join(ld["words"])
        # تحويل من بكسلات الصورة (scale×) إلى وحدات PDF (pt)
        bbox = (ld["x0"]/scale, ld["y0"]/scale, ld["x1"]/scale, ld["y1"]/scale)
        avg_h_pt = (ld["y1"] - ld["y0"]) / scale
        font_size = max(7, min(28, avg_h_pt * 0.8))
        text_elements.append(PageElement(ElementType.TEXT, bbox, text,
                                    {"avg_size": font_size, "ocr": True}))

    text_elements.sort(key=lambda e: (e.bbox[1], e.bbox[0]))

    # دمج الأسطر المتتالية القريبة عمودياً ومتشابهة بحجم الخط في فقرة واحدة
    merged: List[PageElement] = []
    for elem in text_elements:
        if merged:
            prev = merged[-1]
            same_size = abs(prev.font_info.get("avg_size", 12) - elem.font_info.get("avg_size", 12)) < 2
            vertical_gap = elem.bbox[1] - prev.bbox[3]
            line_h = prev.bbox[3] - prev.bbox[1]
            close_enough = 0 <= vertical_gap < line_h * 0.9
            similar_left = abs(elem.bbox[0] - prev.bbox[0]) < 30
            if same_size and close_enough and similar_left:
                prev.content = prev.content.rstrip() + " " + elem.content
                prev.bbox = (min(prev.bbox[0], elem.bbox[0]), prev.bbox[1],
                            max(prev.bbox[2], elem.bbox[2]), elem.bbox[3])
                continue
        merged.append(elem)

    all_elements = elements + merged
    all_elements.sort(key=lambda e: (round(e.bbox[1] / 8), e.bbox[0]))
    return all_elements


def extract_elements(fitz_page: fitz.Page, plumber_page) -> List[PageElement]:
    """يستخرج جميع العناصر من الصفحة، مع تفرّع آلي لـ OCR إن كانت الصفحة ممسوحة."""
    if is_scanned_page(fitz_page):
        print("      🔍 صفحة ممسوحة (سكانر) — تشغيل OCR + كشف الصور...")
        return ocr_extract_elements(fitz_page)
    return _extract_elements_native(fitz_page, plumber_page)


def _extract_elements_native(fitz_page: fitz.Page, plumber_page) -> List[PageElement]:
    """يستخرج جميع العناصر من الصفحة ويصنّفها."""
    elements: List[PageElement] = []
    used_rects = []   # مناطق مستخدمة لتجنب التداخل

    # ── 1. الجداول (pdfplumber أفضل للجداول) ──────────────────────────────
    try:
        tables = plumber_page.extract_tables(
            {"vertical_strategy": "lines", "horizontal_strategy": "lines"}
        )
        table_bboxes = []
        for t_obj in plumber_page.find_tables():
            bb = t_obj.bbox   # (x0,top,x1,bottom)  pdfplumber coords
            # تحويل إحداثيات pdfplumber → fitz  (y محور مقلوب)
            page_h = float(plumber_page.height)
            fx0, fy0 = bb[0], page_h - bb[3]
            fx1, fy1 = bb[2], page_h - bb[1]
            table_bboxes.append((fx0, fy0, fx1, fy1))
            data = t_obj.extract()
            elements.append(PageElement(ElementType.TABLE, (fx0,fy0,fx1,fy1), data))
            used_rects.append(fitz.Rect(fx0, fy0, fx1, fy1))
    except Exception as e:
        print(f"  [تحذير] استخراج الجداول: {e}")

    # ── 2. الصور المضمّنة ────────────────────────────────────────────────────
    img_list = fitz_page.get_images(full=True)
    for img_info in img_list:
        xref = img_info[0]
        # الحصول على bbox الصورة داخل الصفحة
        img_rects = fitz_page.get_image_rects(xref)
        if not img_rects:
            continue
        r = img_rects[0]
        # تجاهل صور الخلفية الكبيرة جداً (>85% من الصفحة)
        page_area = fitz_page.rect.width * fitz_page.rect.height
        img_area  = (r.x1 - r.x0) * (r.y1 - r.y0)
        if img_area / page_area > 0.85:
            continue
        # استخراج بيانات الصورة
        try:
            base_img = fitz_page.parent.extract_image(xref)
            img_bytes = base_img["image"]
            bbox = (r.x0, r.y0, r.x1, r.y1)

            # هل هي رسم بياني؟ نفحص بالحجم والموقع
            etype = ElementType.IMAGE
            # رسوم بيانية عادةً: عرض > 200pt وارتفاع > 150pt
            if (r.x1-r.x0) > 200 and (r.y1-r.y0) > 150:
                etype = ElementType.CHART

            elements.append(PageElement(etype, bbox, img_bytes))
            used_rects.append(r)
        except Exception:
            pass

    # ── 3. كتل النصوص ───────────────────────────────────────────────────────
    blocks = fitz_page.get_text("dict", flags=fitz.TEXT_PRESERVE_WHITESPACE)["blocks"]
    for blk in blocks:
        if blk["type"] != 0:   # 0 = نص
            continue
        r = fitz.Rect(blk["bbox"])
        # تحقق من أن الكتلة لا تتداخل مع جدول/صورة موجودة
        overlap = any(r.intersects(ur) and _overlap_ratio(r, ur) > 0.5 for ur in used_rects)
        if overlap:
            continue

        # تجميع معلومات الخط
        lines_text = []
        font_sizes = []
        for line in blk.get("lines", []):
            for span in line.get("spans", []):
                lines_text.append(span["text"])
                font_sizes.append(span["size"])

        full_text = "\n".join(lines_text).strip()
        if not full_text:
            continue

        avg_size = sum(font_sizes) / len(font_sizes) if font_sizes else 12
        bbox = (r.x0, r.y0, r.x1, r.y1)
        elem = PageElement(ElementType.TEXT, bbox, full_text,
                           {"avg_size": avg_size, "spans": blk})
        elements.append(elem)

    # ── ترتيب العناصر من أعلى لأسفل ثم يسار لأيمن ─────────────────────────
    elements.sort(key=lambda e: (round(e.bbox[1] / 10), e.bbox[0]))
    return elements


def _overlap_ratio(r1: fitz.Rect, r2: fitz.Rect) -> float:
    inter = r1 & r2
    if inter.is_empty:
        return 0.0
    return inter.get_area() / min(r1.get_area(), r2.get_area() + 1e-9)


# ── رسم مستطيلات التمييز على صورة معاينة ────────────────────────────────────

def annotate_page_image(fitz_page: fitz.Page, elements: List[PageElement],
                        scale: float = 2.0) -> Image.Image:
    """يرسم صورة للصفحة مع مستطيلات ترميزية حول كل عنصر."""
    mat = fitz.Matrix(scale, scale)
    pix = fitz_page.get_pixmap(matrix=mat, alpha=False)
    img = Image.frombytes("RGB", [pix.width, pix.height], pix.samples)
    draw = ImageDraw.Draw(img, "RGBA")

    try:
        font = ImageFont.truetype("/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf", 14)
    except Exception:
        font = ImageFont.load_default()

    for elem in elements:
        x0, y0, x1, y1 = [c * scale for c in elem.bbox]
        r, g, b = ELEMENT_COLORS[elem.etype]
        # مستطيل شبه شفاف
        draw.rectangle([x0, y0, x1, y1],
                       outline=(r, g, b, 255),
                       fill=(r, g, b, 25),
                       width=2)
        # تسمية في أعلى اليسار
        label = ELEMENT_LABELS[elem.etype]
        tx, ty = x0 + 3, y0 + 2
        # خلفية للنص
        draw.rectangle([tx - 1, ty - 1, tx + len(label)*7 + 2, ty + 16],
                       fill=(r, g, b, 180))
        draw.text((tx, ty), label, fill="white", font=font)

    return img


# ── إنشاء ملف DOCX مع الحفاظ على التخطيط ────────────────────────────────────

def _set_cell_border(cell, **kwargs):
    """ضبط حدود خلية جدول."""
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    tcBorders = OxmlElement("w:tcBorders")
    for edge in ("top", "left", "bottom", "right", "insideH", "insideV"):
        edge_data = kwargs.get(edge)
        if edge_data:
            tag = OxmlElement(f"w:{edge}")
            for attr, val in edge_data.items():
                tag.set(qn(f"w:{attr}"), str(val))
            tcBorders.append(tag)
    tcPr.append(tcBorders)


def _contains_arabic(text: str) -> bool:
    return any('\u0600' <= ch <= '\u06FF' for ch in text)


def _set_rtl(paragraph):
    """يضبط اتجاه الفقرة من اليمين لليسار (للنصوص العربية)."""
    pPr = paragraph._p.get_or_add_pPr()
    bidi = OxmlElement("w:bidi")
    pPr.append(bidi)


def add_element_to_doc(doc: Document, elem: PageElement,
                        page_w_pt: float, page_h_pt: float,
                        content_w_in: float):
    """يضيف عنصراً واحداً إلى مستند الـ DOCX مع الحفاظ على نسبه المكانية."""

    x0, y0, x1, y1 = elem.bbox
    # نسبة العرض
    rel_x   = x0 / page_w_pt          # من أين يبدأ (0→1)
    rel_w   = (x1 - x0) / page_w_pt   # عرضه النسبي
    elem_w_in = content_w_in * rel_w

    # ── نص ──────────────────────────────────────────────────────────────────
    if elem.etype == ElementType.TEXT:
        para = doc.add_paragraph()
        run  = para.add_run(elem.content)
        avg_sz = elem.font_info.get("avg_size", 12)
        run.font.size = Pt(max(6, avg_sz))

        is_arabic = _contains_arabic(elem.content)
        if is_arabic:
            run.font.name = "Arial"
            _set_rtl(para)
            # محاذاة معكوسة للعربية: يمين = بداية النص
            if rel_x > 0.55:
                para.alignment = WD_ALIGN_PARAGRAPH.LEFT
            elif rel_x > 0.25:
                para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            else:
                para.alignment = WD_ALIGN_PARAGRAPH.RIGHT
        else:
            if rel_x > 0.55:
                para.alignment = WD_ALIGN_PARAGRAPH.RIGHT
            elif rel_x > 0.25:
                para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            else:
                para.alignment = WD_ALIGN_PARAGRAPH.LEFT
        # مسافة بادئة بسيطة
        left_indent = Inches(content_w_in * rel_x * 0.7)
        para.paragraph_format.left_indent = min(left_indent, Inches(content_w_in * 0.5))

    # ── صورة / رسم بياني ─────────────────────────────────────────────────────
    elif elem.etype in (ElementType.IMAGE, ElementType.CHART):
        try:
            img_bytes = elem.content
            if not img_bytes:
                print(f"      [تحذير] عنصر صورة بدون بيانات — تم تجاهله")
                return
            img_stream = io.BytesIO(img_bytes)
            # تحقق من صحة الصورة بدون استخدام verify() الذي يُفسد الـ stream في بعض إصدارات Pillow
            pil_img = Image.open(img_stream)
            pil_img.load()          # يقرأ البيانات فعلياً للتأكد من سلامتها
            img_stream.seek(0)      # نعيد المؤشر لأول الستريم بعد القراءة

            para = doc.add_paragraph()
            if rel_x > 0.55:
                para.alignment = WD_ALIGN_PARAGRAPH.RIGHT
            elif rel_x > 0.25:
                para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            else:
                para.alignment = WD_ALIGN_PARAGRAPH.LEFT

            run = para.add_run()
            display_w = min(elem_w_in, content_w_in)
            if display_w <= 0:
                display_w = content_w_in * 0.5
            run.add_picture(img_stream, width=Inches(display_w))
        except Exception as e:
            import traceback
            print(f"      [خطأ] فشل إضافة صورة ({elem.etype.value}): {e}")
            traceback.print_exc()
            para = doc.add_paragraph(f"[فشل استخراج صورة: {e}]")

    # ── جدول ─────────────────────────────────────────────────────────────────
    elif elem.etype == ElementType.TABLE:
        data = elem.content
        if not data:
            return
        # تنظيف البيانات
        rows = [[str(c or "") for c in row] for row in data]
        if not rows or not rows[0]:
            return

        ncols = max(len(r) for r in rows)
        nrows = len(rows)

        tbl = doc.add_table(rows=nrows, cols=ncols)
        tbl.style = "Table Grid"

        for ri, row in enumerate(rows):
            for ci, cell_txt in enumerate(row):
                if ci >= ncols:
                    break
                cell = tbl.cell(ri, ci)
                cell.text = cell_txt
                # تمييز الصف الأول (رأس الجدول)
                if ri == 0:
                    for run in cell.paragraphs[0].runs:
                        run.bold = True

        doc.add_paragraph()   # سطر فراغ بعد الجدول


